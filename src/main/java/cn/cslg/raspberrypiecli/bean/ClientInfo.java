package cn.cslg.raspberrypiecli.bean;

public class ClientInfo {
    Integer id;
    String password;

    public ClientInfo() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
